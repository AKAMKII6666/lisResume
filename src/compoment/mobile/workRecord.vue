    data() {
      return {
        //用于过度的计算距离
        startRelData: 0,
        //开始移动时的高度
        startTop: 0,
        //开始移动时的虚拟高度
        startVTop: 0,
        //高度
        top: 0,
        //是否已经展示
        isShow: 0,
        //是否锁定位置
        isLocked: true,
        //当前相对位置
        //百分比
        //正数是往上滑动
        //负数是往下滑动
        relPosition: 0,
        //虚拟高度
        vScroHeight: 0,
        //虚拟相对高度
        //百分比
        //正数是往上滑动
        //负数是往下滑动
        vRelScroHeight: 0,
        //指示内容的开始/停止
        start: false,
        //是否显示
        display: 'none',
        //高度
        height: 0,
        //宽度
        width: 0,
        //高度
        kHeight: 0,
        //调起上一个的相对滚动值
        prevVscroll: 0,
        //背景颜色
        background_color: 'rgb(17,17,17)',
        lastRdata: 0,
        //视图特性
        view: {
          //一般页面
          type: 'n-page',
        },
      }
    },
    methods: {
      //隐藏
      hide() {
        if (this.isShow === 1) {
          console.log('画面6隐藏')
          this.display = 'none'
          //指示是否显示
          this.isShow = 0
          this.start = false
        }
      },
      //显示
      show() {
        if (this.isShow === 0) {
          console.log('画面6显示')
          this.display = 'block'
          //指示是否显示
          this.isShow = 1
          //指示是否锁定物理位置
          this.isLocked = true
          //指示是否开始内容滚动
          this.start = true
        }
      },
      resize() {},
      showFromTop() {},
      showFromButton() {},
      //发起上一个视图显示
      prev() {
        var prev = this._mainController.getPrev(this)
        this._mainController.showView(prev, 'fromNext')
      },
      //发起下一个视图显示
      next(_type) {
        if (_type === 'show') {
          var next = this._mainController.getNext(this)
          this._mainController.showView(next, 'fromPrev')
        } else {
          var next = this._mainController.getNext(this)
          this._mainController.hideView(next, 'fromPrev')
        }
      },
      //滚动时的回调函数“钩子”
      scrolling() {
        //如果被标记的虚拟高度大于或等于当时显示时的虚拟高度，就拉起上个画面
        if (Math.abs(this.prevVscroll) >= Math.abs(this.vScroHeight)) {
          this.prev()
        }
        $(this.$refs.mainContainer).css(
          'top',
          this._mainController.nc(
            this.vRelScroHeight,
            0,
            100,
            0,
            this._mainController.screenHeight
          )
        )

        if (this.vRelScroHeight >= 100) {
          var cT = Math.abs(this.vScroHeight) - this._mainController.screenHeight
          $(this.$refs.mainContainer).scrollTop(cT)
          if ($(this.$refs.mainContainer).scrollTop() - 500 < cT - 500 - 10) {
            this.next('show')
            if (typeof this._vs_s === 'undefined') {
              this._vs_s = this.vRelScroHeight
            }
            $(this.$refs.mainContainer).css(
              'top',
              this._mainController.nc(
                this.vRelScroHeight,
                this._vs_s,
                this._vs_s + 100,
                -this._mainController.screenHeight,
                0
              )
            )

            //停止这个画面的计算
            if (this.vRelScroHeight >= this._vs_s + 100) {
              this._mainController.hideView(this)
              var next = this._mainController.getNext(this)
              if (next.prevVscroll === 0) {
                next.prevVscroll = next.vScroHeight
              }
            }
          } else {
            this.next('hide')
          }
        }
      },
      //向上滚出画面时的回调
      dispUpCall() {
        console.log('当前视图离开话面')
      },
      //向下滚出话面时的回调
      dispDownCall() {},
    },
  }
</script>

